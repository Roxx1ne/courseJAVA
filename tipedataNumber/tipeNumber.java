package tipedataNumber;

public class tipeNumber {

    public static void main(String[] args) {
        byte  contohByte = 100;
        System.out.println(contohByte);

        int contohInt = 1000000000;
        System.out.println(contohInt);

        short contohshort = 20000;
        System.out.println(contohshort);

        long contohLong1 = 100000;
        System.out.println(contohLong1);
        long contohLong2 = 100000000L;
        System.out.println(contohLong2);
    }
}